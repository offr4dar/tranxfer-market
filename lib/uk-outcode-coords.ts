/**
 * Returns approximate [lat, lng] for a UK outward code, or null if unknown.
 * Matches on the full outcode first, then falls back to the area prefix.
 */
export function getOutcodeCoords(outcode: string): [number, number] | null {
  const code = outcode.toUpperCase().trim()
  if (AREA_COORDS[code]) return AREA_COORDS[code]

  // Strip trailing digits to get the area prefix (e.g. "SW15" → "SW")
  const area = code.replace(/\d.*$/, '')
  return AREA_COORDS[area] ?? null
}
